"""GET /report/{upload_id} — serve the generated HTML quality report."""

from __future__ import annotations

from fastapi import APIRouter, Depends, HTTPException, status
from fastapi.responses import HTMLResponse

from api.services.storage import StorageManager, get_storage
from logger import get_logger

logger = get_logger("api.report")

router = APIRouter(tags=["Reports"])


@router.get(
    "/report/{upload_id}",
    response_class=HTMLResponse,
    status_code=status.HTTP_200_OK,
    summary="Retrieve the HTML quality report",
    description=(
        "Returns the self-contained HTML quality report generated by "
        "`POST /analyze/{upload_id}`. Chart images are served from `/files/` "
        "so all visuals load correctly in a browser."
    ),
)
def get_report(
    upload_id: str,
    storage: StorageManager = Depends(get_storage),
) -> HTMLResponse:
    if not storage.upload_exists(upload_id):
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail=f"Upload '{upload_id}' not found.",
        )
    if not storage.report_exists(upload_id):
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail=(f"Report for '{upload_id}' not found. Run POST /analyze/{upload_id} first."),
        )

    html = storage.report_html_path(upload_id).read_text(encoding="utf-8")

    # ReportGenerator embeds absolute filesystem paths in <img src="...">.
    # Rewrite them to URL paths so chart images load when served over HTTP.
    storage_root = str(storage.root.resolve())
    html = html.replace(f'src="{storage_root}/', 'src="/files/')
    html = html.replace(f"src='{storage_root}/", "src='/files/")

    return HTMLResponse(content=html, status_code=status.HTTP_200_OK)
